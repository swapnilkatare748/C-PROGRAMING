#include<stdio.h>
int main()
{
	int  i;
	i = 0;
	while (i <= 10)
	{
		printf("\t %d", i * 1);
		printf("\t %d", i * 2);
		printf("\t %d", i * 3);
		printf("\t %d", i * 4);
		printf("\t %d", i * 5);
		printf("\t %d", i * 6);
		printf("\t %d", i * 7);
		printf("\t %d", i * 8);
		printf("\t %d", i * 9);
		printf("\t %d", i * 10);
		i++;
		printf("\n");
	}return 0;
}